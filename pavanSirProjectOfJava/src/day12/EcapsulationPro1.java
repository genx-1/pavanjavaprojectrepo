package day12;

public class EcapsulationPro1 {
	int x ;
	double y ;
	char a ;
	String name ;
	String address;
	
	public int getX() {
		return x;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	public char getA() {
		return a;
	}
	
	public void setA(char a) {
		this.a = a;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getAddress() {
		return address;
	}
	
	public void setAddress(String address) {
		this.address = address;
	}
	
	
			

}
